package ch05.ex02.case01;

public class Box { // 상자라는 객체가 있다. 그 속에 member variable이라는 내용물이 담겨 있다.
	private int a;
	private int b;
	
	public int getA() {
		return a;
	}
	
	public void setA(int a) {
		this.a = a;
	}
	
	public int getB() {
		return b;
	}
	
	public void setB(int b) {
		this.b = b;
	}
}
