package ch02.ex01;

public class C01Variable {
	public static void main(String[] args) {
		boolean bl = false;
		bl = true; // constant 2개에 bl이라는 variable 공간이 하나 있다. 메모리 공간은 총 3개이다.
		
		byte b = 0; // 8bit
		short s = 0; // 16bit
		char c = 0; // 유니코드 문자로 쓰이는 variable이다.
		int i = 0; // 32bit
		long l = 0; // 64bit
		float f = 0.0f;
		double d = 0.0;
		
		String str = "Hello";
		int a;
		a = 0;
		
		int x;
		int y;
		
		int x2, y2;
		
		int x3 = 0, y3 = 0;
	}
}
